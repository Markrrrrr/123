/*****************************************************************************
 函 数 名  : LED_WrDat
 功能描述  : 向OLED写数据
 输入参数  : uchar ucData  
 输出参数  : NONE
 返 回 值  : NONE
*****************************************************************************/
void LED_WrDat(uchar ucData)
{
    uchar i=8;
    Dc=1;
    Scl=0;
    
    while (i--)
    {
        if (ucData & 0x80)
        {
            Sda=1;
        }
        else
        {
            Sda=0;
        }
        
        Scl=1; 
        //delay(10);
        Scl=0;    
        ucData <<= 1;    
    }

    return;
}

/*****************************************************************************
 函 数 名  : LED_WrCmd
 功能描述  : 向OLED写命令
 输入参数  : uchar ucCmd  
 输出参数  : NONE
 返 回 值  : NONE
*****************************************************************************/
void LED_WrCmd(uchar ucCmd)
{
    uchar i = 8;
    Dc=0;
    Scl=0;
    while (i--)
    {
        if (ucCmd & 0x80)
        {
            Sda=1;
        }
        else
        {
            Sda=0;
        }
        Scl=1;
        //delay(10);           
        Scl=0;    
        ucCmd <<= 1;   
    }  

    return;
}


/*****************************************************************************
 函 数 名  : LED_SetPos
 功能描述  : 设置坐标
 输入参数  : uchar ucIdxX  
             uchar ucIdxY  
 输出参数  : NONE
 返 回 值  : NONE
*****************************************************************************/
void LED_SetPos(uchar ucIdxX, uchar ucIdxY)
{ 
    LED_WrCmd(0xb0 + ucIdxY);
    LED_WrCmd(((ucIdxX & 0xf0) >> 4) | 0x10);
    LED_WrCmd((ucIdxX & 0x0f) | 0x00); 
} 

/*****************************************************************************
 函 数 名  : LED_Fill
 功能描述  : 对全屏写入同一个字符函数 
             如 LED_Fill(0x01);    对于某一位0为不亮 1为亮
            ------------------------------------------------------
                                共128列
                  1           1                1
              共  0           0                0
              8   0           0                0
              组  0       ……0     …………   0
              字  0           0                0
              符  0           0                0
              即  0           0                0
              8   0           0                0
              页  1           1                1
              64  0           0                0
              行  .           .                .
                  .           .    …………    .
                  .           .                . 
                  .           .                .
                  0           0                0
            ------------------------------------------------------
 输入参数  : uchar ucData  
 输出参数  : NONE
 返 回 值  : NONE
*****************************************************************************/
void LED_Fill(uchar ucData)
{
    uchar ucPage,ucColumn;
    
    for(ucPage = 0; ucPage < 8; ucPage++)
    {
        LED_WrCmd(0xb0 + ucPage);  //0xb0+0~7表示页0~7
        LED_WrCmd(0x00);           //0x00+0~16表示将128列分成16组其地址在某组中的第几列
        LED_WrCmd(0x10);           //0x10+0~16表示将128列分成16组其地址所在第几组
        for(ucColumn = 0; ucColumn < 128; ucColumn++)
        {
            LED_WrDat(ucData);
        }
    }
} 

