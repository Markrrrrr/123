/*******************************************************************************
**函数名称：void OLEDIO_Init()
**功能描述：OLED功能引脚初始化
**入口参数：无
**输出：无
*******************************************************************************/
void OLEDIO_Init()
{
  PD_DDR_bit.DDR6 = 1; PD_CR1_bit.C16=1; PD_CR2_bit.C26 = 1;//电源控制
  
  
  PF_DDR_bit.DDR4 = 1; PF_CR1_bit.C14 = 1; PF_CR2_bit.C24 = 1;//DC
  PF_DDR_bit.DDR5 = 1; PF_CR1_bit.C15 = 1; PF_CR2_bit.C25 = 1;//RES
  PF_DDR_bit.DDR6 = 1; PF_CR1_bit.C16 = 1; PF_CR2_bit.C26 = 1;//D1
  PF_DDR_bit.DDR7 = 1; PF_CR1_bit.C17 = 1; PF_CR2_bit.C27 = 1;//D0
  
  
  
  
  
  
  OLEDPOW_ON;
}
/*******************************************************************************
**函数名称：void Close_OLED_IO()
**功能描述：OLED功能引脚初始化
**入口参数：无
**输出：无
*******************************************************************************/
void Close_OLED_IO()
{
  PC_ODR_bit.ODR6=0;//OLED
  PC_ODR_bit.ODR5=0;//OLED
  PE_ODR_bit.ODR6=0;//OLED
  PC_ODR_bit.ODR7=0;//OLED
}


